import { type } from "os";
export default {
  name: "advertui",
  props: ["showBtn"],
  methods: {},
  mounted() {
      let that = this;
     new Swiper(".advwsm", {
        speed: 1300,
        resistanceRatio : 0,
        on: {
          slideChangeTransitionStart: function(){
            that.$emit("changeShowbtn")
          },
        },
      pagination: {
        el: ".advertui .swiper-pagination",
        type: "custom",
        renderCustom: function(swiper, current, total) {
          var paginationHtml = " ";
          for (var i = 0; i < total; i++) {
            // 判断是不是激活焦点，是的话添加active类，不是就只添加基本样式类
            if (i === current - 1) {
              paginationHtml +=
                "<span class='swiper-pagination-customs swiper-pagination-customs-active'></span>";
            } else {
              paginationHtml +=
                "<span class='swiper-pagination-customs'></span>";
            }
          }
          return paginationHtml;
        }
      }
    });
  }
};
</script>

